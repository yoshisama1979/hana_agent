# ---------------------------------------------------------------------------
# B-3: ルールが1つもヒットしない場合は cleared が空
# ---------------------------------------------------------------------------
def test_no_match_returns_empty_result():
    journal = _journal([
        {"取引No": "1", "借方勘定科目": "通信費", "貸方勘定科目": "普通預金"},
    ])
    rules = [
        {"name": "振込手数料", "借方勘定科目": "支払手数料"},
    ]

    result = auto_clear(journal, rules)

    assert result.cleared.empty
    assert result.consumed_journal_nos == set()


