# ---------------------------------------------------------------------------
# 複数ルールがある場合、いずれかにヒットすれば消化対象
# ---------------------------------------------------------------------------
def test_multiple_rules_union():
    journal = _journal([
        {"取引No": "1", "借方勘定科目": "支払手数料", "摘要": "振込手数料"},
        {"取引No": "2", "借方勘定科目": "支払手数料", "摘要": "ATM手数料"},
        {"取引No": "3", "借方勘定科目": "通信費", "摘要": "ZOOM"},
    ])
    rules = [
        {"name": "振込手数料", "借方勘定科目": "支払手数料",
         "摘要正規表現": "振込手数料"},
        {"name": "ATM手数料", "借方勘定科目": "支払手数料",
         "摘要正規表現": "ATM手数料"},
    ]

    result = auto_clear(journal, rules)

    assert result.consumed_journal_nos == {"1", "2"}
    # 適用ルール列で見分けられる
    assert set(result.cleared["適用ルール"]) == {"振込手数料", "ATM手数料"}
