"""
auto_clear (F-06) の単体テスト

仕訳帳行のうち、YAMLルールにマッチする「明らかに正しい」行を
一括消化対象としてマーキングする。
"""

import pandas as pd

from accounting.auto_clear import auto_clear


def _journal(rows: list[dict]) -> pd.DataFrame:
    """仕訳帳の最小DataFrameを生成するヘルパー"""
    df = pd.DataFrame(rows)
    for col in ["借方勘定科目", "借方補助科目", "借方金額(円)",
                "貸方勘定科目", "貸方補助科目", "貸方金額(円)",
                "取引No", "取引日", "摘要"]:
        if col not in df.columns:
            df[col] = ""
    return df


# ---------------------------------------------------------------------------
# A-1: 借方勘定科目+貸方勘定科目+摘要正規表現 のルールにヒットする行は消化される
# ---------------------------------------------------------------------------
def test_clears_rows_matching_account_and_description():
    journal = _journal([
        {"取引No": "1", "取引日": "2025/04/21",
         "借方勘定科目": "支払手数料", "借方金額(円)": "140",
         "貸方勘定科目": "普通預金", "貸方補助科目": "SBI", "貸方金額(円)": "140",
         "摘要": "振込手数料"},
        {"取引No": "2", "取引日": "2025/04/21",
         "借方勘定科目": "通信費", "借方金額(円)": "5000",
         "貸方勘定科目": "普通預金", "貸方金額(円)": "5000",
         "摘要": "電気代"},
    ])
    rules = [
        {"name": "振込手数料",
         "借方勘定科目": "支払手数料",
         "貸方勘定科目": "普通預金",
         "摘要正規表現": "振込手数料"},
    ]

    result = auto_clear(journal, rules)

    assert len(result.cleared) == 1
    assert result.cleared.iloc[0]["取引No"] == "1"
    assert result.cleared.iloc[0]["適用ルール"] == "振込手数料"
    assert result.consumed_journal_nos == {"1"}


# ---------------------------------------------------------------------------
# A-2: 一部のフィールドだけ指定したルールでも、指定された条件だけで判定する
# ---------------------------------------------------------------------------
def test_unspecified_fields_are_not_checked():
    journal = _journal([
        {"取引No": "1", "借方勘定科目": "支払手数料",
         "貸方勘定科目": "現金", "摘要": "なんでも"},
        {"取引No": "2", "借方勘定科目": "支払手数料",
         "貸方勘定科目": "普通預金", "摘要": "電気代"},
    ])
    rules = [
        {"name": "支払手数料すべて", "借方勘定科目": "支払手数料"},
    ]

    result = auto_clear(journal, rules)

    # 借方勘定科目だけで判定 → 2件ともヒット
    assert len(result.cleared) == 2
    assert result.consumed_journal_nos == {"1", "2"}


# ---------------------------------------------------------------------------
# A-3: 摘要正規表現は文字列の一部にマッチすればOK（部分一致）
# ---------------------------------------------------------------------------
def test_description_regex_matches_substring():
    journal = _journal([
        {"取引No": "1", "借方勘定科目": "支払手数料", "摘要": "振込手数料 ＊ABC"},
        {"取引No": "2", "借方勘定科目": "支払手数料", "摘要": "ATM手数料"},
    ])
    rules = [
        {"name": "振込", "借方勘定科目": "支払手数料",
         "摘要正規表現": "振込手数料"},
    ]

    result = auto_clear(journal, rules)

    assert result.consumed_journal_nos == {"1"}


# ---------------------------------------------------------------------------
# A-4: 金額条件を入れた場合、その金額以外はマッチしない
# ---------------------------------------------------------------------------
def test_amount_condition_filters_strictly():
    journal = _journal([
        {"取引No": "1", "借方勘定科目": "支払手数料", "借方金額(円)": "140"},
        {"取引No": "2", "借方勘定科目": "支払手数料", "借方金額(円)": "330"},
    ])
    rules = [
        {"name": "140円のみ", "借方勘定科目": "支払手数料", "借方金額": 140},
    ]

    result = auto_clear(journal, rules)

    assert result.consumed_journal_nos == {"1"}


