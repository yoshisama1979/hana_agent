# ---------------------------------------------------------------------------
# R-B1: 同じ補助科目・同金額が N:N の場合、取引日昇順で 1:1 ペアリング
# ---------------------------------------------------------------------------
def test_pairs_n_to_n_in_chronological_order():
    journal = _journal([
        # 請求3件
        {"取引No": "10", "取引日": "2025/04/18",
         "借方勘定科目": "売掛金", "借方補助科目": "株式会社B", "借方金額(円)": "100000",
         "貸方勘定科目": "売上高", "摘要": "請求4月"},
        {"取引No": "11", "取引日": "2025/05/19",
         "借方勘定科目": "売掛金", "借方補助科目": "株式会社B", "借方金額(円)": "100000",
         "貸方勘定科目": "売上高", "摘要": "請求5月"},
        {"取引No": "12", "取引日": "2025/06/16",
         "借方勘定科目": "売掛金", "借方補助科目": "株式会社B", "借方金額(円)": "100000",
         "貸方勘定科目": "売上高", "摘要": "請求6月"},
        # 入金3件
        {"取引No": "20", "取引日": "2025/05/09",
         "借方勘定科目": "普通預金", "借方金額(円)": "100000",
         "貸方勘定科目": "売掛金", "貸方補助科目": "株式会社B", "貸方金額(円)": "100000",
         "摘要": "入金1"},
        {"取引No": "21", "取引日": "2025/06/10",
         "借方勘定科目": "普通預金", "借方金額(円)": "100000",
         "貸方勘定科目": "売掛金", "貸方補助科目": "株式会社B", "貸方金額(円)": "100000",
         "摘要": "入金2"},
        {"取引No": "22", "取引日": "2025/07/10",
         "借方勘定科目": "普通預金", "借方金額(円)": "100000",
         "貸方勘定科目": "売掛金", "貸方補助科目": "株式会社B", "貸方金額(円)": "100000",
         "摘要": "入金3"},
    ])

    result = reconcile_receivables(journal)

    assert len(result.matched) == 3
    pairs = result.matched.sort_values("請求_取引日").reset_index(drop=True)
    assert pairs.iloc[0]["請求_取引No"] == "10"
    assert pairs.iloc[0]["入金_取引No"] == "20"
    assert pairs.iloc[1]["請求_取引No"] == "11"
    assert pairs.iloc[1]["入金_取引No"] == "21"
    assert pairs.iloc[2]["請求_取引No"] == "12"
    assert pairs.iloc[2]["入金_取引No"] == "22"


