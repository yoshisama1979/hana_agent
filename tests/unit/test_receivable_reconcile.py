# ---------------------------------------------------------------------------
# R-C1: 請求3件・入金2件の場合、最古2件の請求が消化され、最新1件は残る
# ---------------------------------------------------------------------------
def test_partial_match_when_invoices_exceed_deposits():
    journal = _journal([
        # 請求3件
        {"取引No": "10", "取引日": "2025/04/18",
         "借方勘定科目": "売掛金", "借方補助科目": "株式会社C", "借方金額(円)": "55000",
         "貸方勘定科目": "売上高"},
        {"取引No": "11", "取引日": "2025/05/19",
         "借方勘定科目": "売掛金", "借方補助科目": "株式会社C", "借方金額(円)": "55000",
         "貸方勘定科目": "売上高"},
        {"取引No": "12", "取引日": "2025/06/16",
         "借方勘定科目": "売掛金", "借方補助科目": "株式会社C", "借方金額(円)": "55000",
         "貸方勘定科目": "売上高"},
        # 入金2件
        {"取引No": "20", "取引日": "2025/05/09",
         "借方勘定科目": "普通預金", "借方金額(円)": "55000",
         "貸方勘定科目": "売掛金", "貸方補助科目": "株式会社C", "貸方金額(円)": "55000"},
        {"取引No": "21", "取引日": "2025/06/10",
         "借方勘定科目": "普通預金", "借方金額(円)": "55000",
         "貸方勘定科目": "売掛金", "貸方補助科目": "株式会社C", "貸方金額(円)": "55000"},
    ])

    result = reconcile_receivables(journal)

    assert len(result.matched) == 2
    # 取引No 12（最新の請求）は消化されない
    assert "12" not in result.consumed_journal_nos
    assert {"10", "11", "20", "21"}.issubset(result.consumed_journal_nos)


