"""
売掛金照合 (F-05) の単体テスト

仕訳帳内の「請求（借方=売掛金）」と「入金（貸方=売掛金）」を
補助科目+金額でグループ化し、取引日順に1:1ペアリングする。
"""

import pandas as pd

from accounting.receivable_reconcile import reconcile_receivables


def _journal(rows: list[dict]) -> pd.DataFrame:
    """仕訳帳の最小DataFrameを生成するヘルパー（必須カラムを補完）"""
    df = pd.DataFrame(rows)
    for col in ["借方勘定科目", "借方補助科目", "借方金額(円)",
                "貸方勘定科目", "貸方補助科目", "貸方金額(円)",
                "取引No", "取引日", "摘要"]:
        if col not in df.columns:
            df[col] = ""
    return df


# ---------------------------------------------------------------------------
# R-A1: 補助科目+金額が一致し、入金日 > 請求日 ならペアになる
# ---------------------------------------------------------------------------
def test_pairs_invoice_with_later_deposit_of_same_amount():
    journal = _journal([
        {"取引No": "1", "取引日": "2025/04/18",
         "借方勘定科目": "売掛金", "借方補助科目": "株式会社A", "借方金額(円)": "110000",
         "貸方勘定科目": "売上高", "摘要": "請求"},
        {"取引No": "2", "取引日": "2025/05/09",
         "借方勘定科目": "普通預金", "借方金額(円)": "110000",
         "貸方勘定科目": "売掛金", "貸方補助科目": "株式会社A", "貸方金額(円)": "110000",
         "摘要": "入金"},
    ])

    result = reconcile_receivables(journal)

    assert len(result.matched) == 1
    pair = result.matched.iloc[0]
    assert pair["請求_取引No"] == "1"
    assert pair["入金_取引No"] == "2"
    assert result.consumed_journal_nos == {"1", "2"}


